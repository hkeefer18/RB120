class CircularQueue
  def initialize(max_size)
    @array = []
    @max_size = max_size
  end

  def enqueue(new_ele)
    @array << new_ele
    @array.shift if @array.size > @max_size
  end

  def dequeue
    @array.shift
  end
end

queue = CircularQueue.new(3)
puts queue.dequeue == nil

queue.enqueue(1)
queue.enqueue(2)
puts queue.dequeue == 1

queue.enqueue(3)
queue.enqueue(4)
puts queue.dequeue == 2

queue.enqueue(5)
queue.enqueue(6)
queue.enqueue(7)
puts queue.dequeue == 5
puts queue.dequeue == 6
